package org.suych.fm.util.generate.model.xml;

/**
 * XML公共节点
 */
public class XmlCommonNode {

	/**
	 * id
	 */
	private String id;
	
	

	@Override
	public String toString() {
		return "XmlCommonNode [id=" + id + "]";
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
