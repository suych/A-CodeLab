package org.suych.fm.constant;

/**
 * 方法-非访问控制修饰符
 */
public class ConstantMethodNonAccessModifier {

	/**
	 * 默认无修饰
	 */
	public static final ConstantMethodNonAccessModifier DEFAULT = new ConstantMethodNonAccessModifier();

	/**
	 * static
	 */
	public static final ConstantMethodNonAccessModifier STATIC = new ConstantMethodNonAccessModifier();

	/**
	 * final
	 */
	public static final ConstantMethodNonAccessModifier FINAL = new ConstantMethodNonAccessModifier();

	/**
	 * abstract
	 */
	public static final ConstantMethodNonAccessModifier ABSTRACT = new ConstantMethodNonAccessModifier();

	/**
	 * synchronized
	 */
	public static final ConstantMethodNonAccessModifier SYNCHRONIZED = new ConstantMethodNonAccessModifier();
}
