package org.suych.fm.constant;

/**
 * 方法-访问控制修饰符
 */
public class ConstantMethodAccessModifier {

	/**
	 * 默认无修饰
	 */
	public static final ConstantMethodAccessModifier DEFAULT = new ConstantMethodAccessModifier();

	/**
	 * public
	 */
	public static final ConstantMethodAccessModifier PUBLIC = new ConstantMethodAccessModifier();

	/**
	 * private
	 */
	public static final ConstantMethodAccessModifier PRIVATE = new ConstantMethodAccessModifier();

	/**
	 * protected
	 */
	public static final ConstantMethodAccessModifier PROTECTED = new ConstantMethodAccessModifier();
}
