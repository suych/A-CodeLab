	public static final ConstantFieldNonAccessModifier VOLATILE = new ConstantFieldNonAccessModifier();

	/**
	 * transient
	 */
	public static final ConstantFieldNonAccessModifier TRANSIENT = new ConstantFieldNonAccessModifier();
