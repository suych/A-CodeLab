	public static final ConstantFieldAccessModifier PUBLIC = new ConstantFieldAccessModifier();

	/**
	 * private
	 */
	public static final ConstantFieldAccessModifier PRIVATE = new ConstantFieldAccessModifier();

	/**
	 * protected
	 */
	public static final ConstantFieldAccessModifier PROTECTED = new ConstantFieldAccessModifier();
